    private void setupProductPanel() {
        productsPanel = new JPanel();
        productsPanel.setLayout(new WrapLayout(FlowLayout.LEFT, 30, 13));
        jScrollPane1.setViewportView(productsPanel);
        jScrollPane1.setHorizontalScrollBarPolicy(ScrollPaneConstants.HORIZONTAL_SCROLLBAR_NEVER);
        jScrollPane1.setVerticalScrollBarPolicy(ScrollPaneConstants.VERTICAL_SCROLLBAR_AS_NEEDED);
    }

    private void loadProducts() {
        ProductDao dao = new ProductDao();
        java.util.List<Product> products = dao.getAllProducts();

        productsPanel.removeAll();
        for (Product product : products) {
            ProductCard card = new ProductCard(product);
            card.setProduct(product);
            productsPanel.add(card);
        }
        productsPanel.revalidate();
        productsPanel.repaint();
    }
    
