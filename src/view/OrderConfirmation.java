    public void setOrderDetails(model.Order order, double totalAmount) {
    OrderId.setText(order.getOrderId() == 0 ? "N/A" : String.valueOf(order.getOrderId()));
    CustomerID.setText(String.valueOf(order.getUserId()));
    Amount.setText(String.format("%.2f", totalAmount));

    // Fetch and set username
    UserDao userDao = new UserDao();
    String username = userDao.getUsernameById(order.getUserId());
    CustomerName.setText(username);
}
