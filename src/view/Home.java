    private void loadTrending() {
        jPanel2.removeAll();
        String[] trendingImages = {"1.png", "2.png", "moon.png"};
        for (String imgName : trendingImages) {
            Trending card = new Trending("/images/" + imgName);
            jPanel2.add(card);
        }
        jPanel2.revalidate();
        jPanel2.repaint();
    }
    

