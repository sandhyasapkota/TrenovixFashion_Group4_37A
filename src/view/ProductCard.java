    public void setProduct(model.Product p) {
        this.product = p;
        name.setText(p.getName());

        // Image set garne (if available)
        if (p.getImageUrl() != null && !p.getImageUrl().isEmpty()) {
            ImageIcon icon = new ImageIcon(p.getImageUrl());
            // Resize image to fit label (e.g. 100x100)
            Image img = icon.getImage().getScaledInstance(160, 150, Image.SCALE_SMOOTH);
            image.setIcon(new ImageIcon(img));
            image.setText(""); // Remove default text
        } else {
            image.setIcon(null);
            image.setText("No Image");
        }
    }

